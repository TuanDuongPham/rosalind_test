from helper.extract_fasta_seqences import extract_fasta_sequences


def check_overlap(sequence_1, sequence_2, k_length):
    k_suffix = sequence_1[-3:]
    k_prefix = sequence_2[0:3]

    if k_suffix == k_prefix:
        return True

    return False


def overlap_grap(fasta_file, k_length):
    dna_sequence_dict = extract_fasta_sequences(fasta_file)
    grapg_edges = []

    for dna_id_1, dna_sequence_1 in dna_sequence_dict.items():
        for dna_id_2, dna_sequence_2 in dna_sequence_dict.items():
            if dna_id_1 == dna_id_2:
                continue

            check_overlap_result = check_overlap(dna_sequence_1, dna_sequence_2, k_length)
            if check_overlap_result:
                grapg_edges.append((dna_id_1, dna_id_2))

    return grapg_edges


