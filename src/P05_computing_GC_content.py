from helper.extract_fasta_seqences import extract_fasta_sequences


def compute_CG_propotion(fasta_file_contents):
    dna_sequence_dict = extract_fasta_sequences(fasta_file_contents)

    max_cg_content = None
    for sequence_id, dna_sequence in dna_sequence_dict.items():
        c_count = dna_sequence.count('C')
        g_count = dna_sequence.count('G')
        cg_content = (c_count + g_count) / len(dna_sequence) * 100

        if max_cg_content is None or cg_content > max_cg_content[1]:
            max_cg_content = (sequence_id, cg_content)

    return max_cg_content


