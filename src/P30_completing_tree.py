"""
- A graph is connected if there is a path connecting any two nodes
- A tree is a connected graph with no cycles

- Assume that the given grahp is a "forest" which posibly disconnected that have c tree (each tree is a connected component)
- Let n be the total nodes and m be the total edges of the given graph
- Suppose that we want to connect all the tree together, then we have to connect each tree with at least another tree
- Each time we connect two disconnect tree together, 1 edge is used. Since we have c tree, therefore we need total of c-1 edge    (1)

- For each tree with V nodes, siince they are connected and contain no cycles, they must have exactly V-1 edges
=> The total edges of all tree = sum of (Vi - 1) with i = 1,2,..,c
=> Total edges = sum of (Vi) - c*1     # because there are c tree and we substract 1 with each tree
=> Total edges = total given edge - c
=> c = total edges - total given edge    (2)

From (1) and (2), the nummber of needed egdes to complete a tree = total edge - total given edge - 1
"""


def handle_raw_input(raw_input):
    lines = raw_input.strip().splitlines()
    n = int(lines[0])

    edges = []
    for line in lines[1:]:
        u, v = map(int, line.split())
        edges.append((u, v))

    return n, edges


def completing_tree_simple(n, edges):
    m = len(edges)
    return (n - m - 1)


