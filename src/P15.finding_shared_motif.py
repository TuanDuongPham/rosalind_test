from helper.extract_fasta_seqences import extract_fasta_sequences
from helper.is_substring_in_all import is_substring_in_all


def finding_shared_motif(fasta_sequences):
    fasta_sequences_dict = extract_fasta_sequences(fasta_sequences)
    sequences = list(fasta_sequences_dict.values())

    longest_common_substring = ""

    for i in range(len(sequences[0])):
        for j in range(i + 1, len(sequences[0]) + 1):
            candidate = sequences[0][i:j]
            if is_substring_in_all(candidate, sequences):
                if len(candidate) > len(longest_common_substring):
                    longest_common_substring = candidate

    return longest_common_substring


